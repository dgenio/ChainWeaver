"""Benchmark report packaging for README/docs evidence (issue #207).

Turns the standalone benchmark scripts into versioned, shareable artifacts so
README/docs claims cite generated numbers instead of hand-written ones.  The
report aggregates:

1. **Latency** — naive-vs-compiled under a declared mocked model-delay profile
   (``bench_naive_vs_compiled.py``, issue #29).
2. **Decisions avoided** — model-mediated routing calls a compiled flow removes.
3. **Cost avoided** — priced against the maintained ``PROVIDER_PRICES`` table
   (``chainweaver.cost``, issue #156).
4. **Correctness** — data-corruption elimination (``bench_correctness.py``,
   issue #103).
5. **Environment metadata** — Python version, ChainWeaver version, OS, and the
   benchmark commit SHA.
6. **Caveats** — synthetic tools, mocked delays, not a cross-framework claim.

Reproducibility matters more than impressive numbers: every figure here is
generated by code in this repo with no network access, no API keys, and no
paid LLM calls.

Run it from the repository root::

    python benchmarks/report.py
    python benchmarks/report.py --output-dir benchmarks/results
"""

from __future__ import annotations

import argparse
import json
import platform
import subprocess
import sys
from importlib import metadata
from pathlib import Path
from typing import Any

# Make sibling benchmark modules importable whether this file is run as a
# script (``python benchmarks/report.py``) or imported by path from a test.
_HERE = Path(__file__).resolve().parent
if str(_HERE) not in sys.path:
    sys.path.insert(0, str(_HERE))

import bench_correctness as correctness  # noqa: E402
import bench_naive_vs_compiled as latency  # noqa: E402

from chainweaver.cost import compute_cost_report  # noqa: E402

# Declared mocked model-delay profile for the latency leg. Documented as an
# assumption, not a measurement of any real provider.
DEFAULT_LATENCY_CASES: list[tuple[int, float, float]] = [
    (2, 0.100, 0.000),
    (5, 0.200, 0.000),
    (10, 0.200, 0.010),
]

# Provider/model used to price "cost avoided" in the report. The figure is an
# estimate that depends entirely on this snapshot.
DEFAULT_PROVIDER = "anthropic"
DEFAULT_MODEL = "claude-sonnet-4-6"


def _git_sha() -> str:
    """Return the current commit SHA, or ``"unknown"`` outside a git checkout."""
    try:
        out = subprocess.run(
            ["git", "rev-parse", "HEAD"],
            capture_output=True,
            text=True,
            check=True,
            cwd=_HERE,
        )
        return out.stdout.strip() or "unknown"
    except (OSError, subprocess.CalledProcessError):  # pragma: no cover - env dependent
        return "unknown"


def _chainweaver_version() -> str:
    try:
        return metadata.version("chainweaver")
    except metadata.PackageNotFoundError:  # pragma: no cover - dev checkout edge
        return "unknown"


def _environment() -> dict[str, Any]:
    return {
        "python_version": platform.python_version(),
        "chainweaver_version": _chainweaver_version(),
        "os": f"{platform.system()} {platform.release()}",
        "commit_sha": _git_sha(),
    }


CAVEATS: list[str] = [
    "Tools are synthetic and LLM round-trips are simulated with time.sleep — "
    "the latency figures are a proxy for model-delay only, not a real provider "
    "measurement.",
    "Cost-avoided dollars are an estimate priced against a dated PROVIDER_PRICES "
    "snapshot; they scale with the assumed tokens-per-call.",
    "Corruption rates come from a configurable, seeded model, not from live LLM behaviour.",
    "No cross-framework comparison is implied; these benchmarks measure compiled "
    "vs naive chaining only.",
]


def build_report(
    *,
    latency_cases: list[tuple[int, float, float]] | None = None,
    latency_repeats: int = 3,
    correctness_runs: int = 100,
    provider: str = DEFAULT_PROVIDER,
    model: str = DEFAULT_MODEL,
) -> dict[str, Any]:
    """Assemble the aggregate benchmark report as a JSON-serializable dict.

    Args:
        latency_cases: ``(n_steps, llm_delay_s, tool_delay_s)`` tuples for the
            latency leg. Defaults to :data:`DEFAULT_LATENCY_CASES`.
        latency_repeats: Repeats per latency case (median reporting).
        correctness_runs: Runs per correctness scenario.
        provider: Provider key for cost pricing.
        model: Model key for cost pricing.

    Returns:
        A dict with ``environment``, ``latency``, ``cost``, ``correctness``,
        and ``caveats`` keys.
    """
    cases = latency_cases or DEFAULT_LATENCY_CASES
    latency_report = latency.run_cases(cases, repeats=latency_repeats)

    decisions_avoided = sum(case["llm_calls_avoided"] for case in latency_report["cases"])

    # Price the largest latency case's avoided routing calls.
    max_steps = max(n for n, _, _ in cases)
    cost = compute_cost_report(
        steps_executed=max_steps,
        actual_execution_ms=0.0,
        provider=provider,
        model=model,
    )

    correctness_report = correctness.run_scenarios(runs=correctness_runs)

    return {
        "environment": _environment(),
        "parameters": {
            "latency_cases": [
                {"n_steps": n, "llm_delay_ms": llm * 1000.0, "tool_delay_ms": tool * 1000.0}
                for n, llm, tool in cases
            ],
            "latency_repeats": latency_repeats,
            "correctness_runs": correctness_runs,
            "cost_provider": provider,
            "cost_model": model,
        },
        "latency": latency_report,
        "decisions_avoided": decisions_avoided,
        "cost": {
            "provider": provider,
            "model": model,
            "price_as_of": cost.profile.price_as_of,
            "priced_steps": max_steps,
            "llm_calls_avoided": cost.llm_calls_avoided,
            "cost_saved_usd": cost.cost_saved_usd,
        },
        "correctness": correctness_report,
        "caveats": CAVEATS,
    }


def render_markdown(report: dict[str, Any]) -> str:
    """Render the report as a Markdown document for docs/README inclusion."""
    env = report["environment"]
    cost = report["cost"]
    lines: list[str] = [
        "# ChainWeaver Benchmark Report",
        "",
        "> Generated by `benchmarks/report.py`. Do not edit by hand.",
        "",
        "## Environment",
        "",
        f"- Python: {env['python_version']}",
        f"- ChainWeaver: {env['chainweaver_version']}",
        f"- OS: {env['os']}",
        f"- Commit: `{env['commit_sha']}`",
        "",
        "## Headline numbers",
        "",
        f"- Model-mediated decisions avoided (across latency cases): "
        f"**{report['decisions_avoided']}**",
        f"- Estimated cost avoided for a {cost['priced_steps']}-step flow "
        f"(priced against {cost['provider']}/{cost['model']}, as of "
        f"{cost['price_as_of']}): **${cost['cost_saved_usd']:.4f}**",
        "",
        "## Latency (median ms, smaller is better)",
        "",
        "| Steps | LLM delay (ms) | Naive total | Compiled total | Speedup |",
        "|------:|---------------:|------------:|---------------:|--------:|",
    ]
    for case in report["latency"]["cases"]:
        lines.append(
            f"| {case['n_steps']} | {case['llm_delay_ms']:.0f} | "
            f"{case['naive']['total_duration_ms']['median']:.2f} | "
            f"{case['compiled']['total_duration_ms']['median']:.2f} | "
            f"{case['median_speedup_factor']:.1f}x |"
        )
    lines += [
        "",
        "## Correctness (data integrity)",
        "",
        "| Scenario | Steps | Naive corruption rate | Compiled corruption rate |",
        "|----------|------:|----------------------:|-------------------------:|",
    ]
    for case in report["correctness"]["cases"]:
        lines.append(
            f"| {case['scenario']} | {case['n_steps']} | "
            f"{case['naive']['corruption_rate']:.1%} | "
            f"{case['compiled']['corruption_rate']:.1%} |"
        )
    lines += ["", "## Caveats", ""]
    lines += [f"- {c}" for c in report["caveats"]]
    lines.append("")
    return "\n".join(lines)


def write_artifacts(report: dict[str, Any], output_dir: Path) -> tuple[Path, Path]:
    """Write ``latest.json`` and ``latest.md`` into ``output_dir``."""
    output_dir.mkdir(parents=True, exist_ok=True)
    json_path = output_dir / "latest.json"
    md_path = output_dir / "latest.md"
    json_path.write_text(json.dumps(report, indent=2), encoding="utf-8")
    md_path.write_text(render_markdown(report), encoding="utf-8")
    return json_path, md_path


def _parse_args(argv: list[str] | None = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Generate the aggregate benchmark report.")
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=_HERE / "results",
        help="Directory for latest.json / latest.md. Default: benchmarks/results.",
    )
    parser.add_argument("--latency-repeats", type=int, default=3, help="Repeats per latency case.")
    parser.add_argument("--correctness-runs", type=int, default=100, help="Runs per scenario.")
    return parser.parse_args(argv)


def main(argv: list[str] | None = None) -> int:
    args = _parse_args(argv)
    report = build_report(
        latency_repeats=args.latency_repeats,
        correctness_runs=args.correctness_runs,
    )
    json_path, md_path = write_artifacts(report, args.output_dir)
    print(render_markdown(report))
    print(f"\nArtifacts written:\n  {json_path}\n  {md_path}")
    return 0


if __name__ == "__main__":  # pragma: no cover
    sys.exit(main())
